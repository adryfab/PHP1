<?php
/**
 * Reemplaza todos los acentos por sus equivalentes sin ellos
 *
 * @param $string
 *  string la cadena a sanear
 *
 * @return $string
 *  string saneada
 */
function sanear_string($string)
{
 
    $string = trim($string);
 
    $string = str_replace(
        array('á', 'à', 'ä', 'â', 'ª', 'Á', 'À', 'Â', 'Ä'),
        array('a', 'a', 'a', 'a', 'a', 'A', 'A', 'A', 'A'),
        $string
    );
 
    $string = str_replace(
        array("é", "è", "ë", "ê"),
        "e",
        $string
    );
 
    $string = str_replace(
        array("É", "È", "Ê", "Ë"),
        "E",
        $string
    );

    $string = str_replace(
        array('í', 'ì', 'ï', 'î', 'Í', 'Ì', 'Ï', 'Î'),
        array('i', 'i', 'i', 'i', 'I', 'I', 'I', 'I'),
        $string
    );
 
    $string = str_replace(
        array('ó', 'ò', 'ö', 'ô', 'Ó', 'Ò', 'Ö', 'Ô'),
        array('o', 'o', 'o', 'o', 'O', 'O', 'O', 'O'),
        $string
    );
 
    $string = str_replace(
        array('ú', 'ù', 'ü', 'û', 'Ú', 'Ù', 'Û', 'Ü'),
        array('u', 'u', 'u', 'u', 'U', 'U', 'U', 'U'),
        $string
    );
 
    $string = str_replace(
        array('ñ', 'Ñ', 'ç', 'Ç'),
        array('n', 'N', 'c', 'C',),
        $string
    );
 
    //Esta parte se encarga de eliminar cualquier caracter extraño
    //simbolos de teclado
    $string = str_replace(
        array(",", ".", ";", ":"),
        "",
        $string);
    //simbolos con comilla simple
    $string = str_replace(
      array('"'),
      "", 
      $string);
    //simbolos de pagina (enter, tab, etc)
    $string = str_replace(
        array("\t", "\n"),
        "",
        $string);
    //simbolos adicionales
    $string = str_replace(
        array("·"),
        "",
        $string);
    //representacion de simbolos especiales
    $string = str_replace(
      array(PHP_EOL), 
      "", 
      $string);
  
    return $string;
}
?>
